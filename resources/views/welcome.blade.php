                <p>Persaudaraan Muslimah (Salimah) adalah organisasi kemasyarakatan perempuan yang didirikan pada 8 Maret 2000 oleh para aktivis dakwah perempuan sebagai wadah pemberdayaan muslimah Indonesia. Salimah hadir dengan kepedulian terhadap kondisi perempuan, keluarga, dan masyarakat melalui berbagai program sosial, pendidikan, dakwah, serta perlindungan anak guna membentuk perempuan yang cerdas, mandiri, dan bermanfaat bagi lingkungan sekitar.</p><br>
